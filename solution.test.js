const solution = require('./index');

test("soultion { '2019-01-01': 100, '2019-01-04':115, } to equal { '2019-01-01': 100, '2019-01-02': 105, '2019-01-03': 110, '2019-01-04': 115 }", () => {
    expect(solution({ '2019-01-01': 100, '2019-01-04':115, } ) ).toMatchObject({ '2019-01-01': 100,'2019-01-02': 105,'2019-01-03': 110,'2019-01-04': 115 });
});
test("soultion { '2019-01-10': 10, '2019-01-11':20, '2019-01-13':10 } to equal { '2019-01-01': 100, '2019-01-02': 105, '2019-01-03': 110, '2019-01-04': 115 }", () => {
    expect(solution({ '2019-01-10': 10, '2019-01-11':20,'2019-01-12':15,'2019-01-13':10 } ) ).toMatchObject({ '2019-01-10': 10,'2019-01-11': 20,'2019-01-12': 15,'2019-01-13': 10 });
});